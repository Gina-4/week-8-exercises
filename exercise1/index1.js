// Playing around with objects
let car = {
    make: 'Audi',
    year: 2025,
    color: 'black'
}

console.dir(car) // displays list of properties of an object

let person = {
    name: 'Alice',
    age: 24,
    occupation: 'teacher'
}
console.log(person.occupation) //dot notation

let fruit = {
    color: 'red',
    taste: 'sweet',
    seeds: 1,
}
console.log(fruit["taste"]) //bracket notation

